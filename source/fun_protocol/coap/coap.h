/**
 *
 *0               8     16            32
 *-------------------------------------------------------------
 *| Ver | T | TKL | Code | Message ID |
 *------------------------------------------------
 *|     Token(如果存在)                 |
 *-------------------------------------------
 *|     Options(如果存在)               |
 *--------------------------------------------
 *| 0xFF          |  Payload(如果存在)  |
 *----------------------------------------------
 *
 * Ver 版本号 2bit 固定 01
 *
 * T 报文类型 2bit   有图1-1种报文类型
 *
 *              图1-1
 * ---------------------------------------------
 * |    类型      |           描述              |
 * ----------------------------------------------
 * |  CON报文     | confirmbable 需要被确认的报文 |
 * ----------------------------------------------
 * |  NON报文     | non-confirmable 不需要被确认 |
 * ----------------------------------------------
 * |  ACK报文     |  Acknowledgement 应答报文   |
 * ---------------------------------------------
 * |  Reset报文   |  复位报文                   |
 * --------------------------------------------
 *
 *
 * TKL 标签长度指示 4bit 用于指示后面的Token占用几个字节
 * 取值如下：
 *  0000    没有token
 *  0001    占用1字节
 *  0010    占用2字节
 *  0100    占用4字节
 *
 * Code 8bit 前3bit为 Class部分   后5bit为Detail部分 c.dd形式描述
 * 注：   请求报文中Code表示为 请求方法
 *       响应报文中Code表示为  响应状态
 *
 * ------------------------------------------------------
 * |    Code值   |               CoAP请求方法            |
 * ------------------------------------------------------
 * |    0.01     |          GET方法                     |
 * ------------------------------------------------------
 * |    0.02     |          POST方法                    |
 * ------------------------------------------------------
 * |    0.03     |          PUT方法                     |
 * ------------------------------------------------------
 * |    0.04     |          DELETE方法                  |
 * ------------------------------------------------------
 *
 * ------------------------------------------------------
 * |    Code值    |              CoAP响应状态            |
 * ------------------------------------------------------
 * |    0.00      |             空报文                  |
 * ------------------------------------------------------
 * |    2.xx      |         正确响应                    |
 * -----------------------------------------------------
 * |    4.xx      |         客户端错误                  |
 * -----------------------------------------------------
 * |    5.xx      |         服务端错误                  |
 * -----------------------------------------------------
 *
 *
 * Message ID,报文序号
 *
 * Token 标签
 *      CoAP有3种不同的请求/响应 模式
 *          携带模式、分离模式、非确认模式
 *
 * Options 选项
 *
 * 0xFF 分割符 (和http的\r\n类似)
 *
 * Payload 载荷
*/
